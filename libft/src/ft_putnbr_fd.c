#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	aux;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		if (n == 0)
			write(fd, "0", 1);
		if (n > 0 && n <= 9)
		{
			aux = n + '0';
			write(fd, &aux, 1);
		}
		if (n > 9)
		{
			ft_putnbr_fd(n / 10, fd);
			ft_putnbr_fd(n % 10, fd);
		}
	}
}
